"""
build_dict.py — Generate schemas/<slug>/hakka_<slug>.dict.yaml from CSV sources.

Inputs:
  sources/chars-<slug>.csv          Single-character readings. One row per
                                    (character, reading). CSV columns (flexible):
                                    either
                                        char,code
                                    or the hkilang/TTS format
                                        id,char,left_col,right_col,hint
                                    where right_col is the Hagfa Pinyim reading
                                    we want.
  sources/words-<slug>.csv          Multi-character vocabulary (char,pron).
                                    Produced by fetch_sources.py from
                                    hkilang/TTS hakka_words.csv.
  sources/flashcards-<slug>.csv     Phrase entries. CSV columns:
                                    普通中文,客家汉字,Hakka Pronunciation,English Definition
                                    Only the 客家汉字 and Hakka Pronunciation
                                    columns are used.
  sources/phrases/<slug>/*.csv      Optional additional phrase files, same
                                    schema as flashcards CSV.
sources/tw-hakka-word-weighting.txt Optional. Rime character frequency corpus
                                    used to weight single characters by how
                                    commonly they appear in written Chinese.
                                    Source:
                                    https://github.com/rime/rime-essay/blob/master/essay.txt
                                    If present it is loaded automatically.
                                    Override the path with --char-freq.  sources/mandarin-char-ranks.txt   Optional. Mandarin character frequency data
                                    derived from hanzicraft.com and Wiktionary.
                                    Generated by build/fetch_mandarin_ranks.py.
                                    Auto-loaded as a fallback for characters not
                                    covered by the primary corpus.
Output:
  schemas/<slug>/hakka_<slug>.dict.yaml

Weights (seven tiers):
  - Curated flashcard phrases:         1000  (hand-checked, always win)
  - Vocabulary words, corpus high:      500  (top 10% by corpus frequency)
  - Vocabulary words, corpus mid:       300  (next 30%)
  - Vocabulary words, corpus low:       150  (next 30%)
  - Vocabulary words, no corpus data:   100 / 80  (short / long, not in corpus)
  - Single characters, high freq:        90  (top 10% by corpus frequency)
  - Single characters, mid freq:         50  (next 40%)
  - Single characters, low freq:         20  (next 40%)
  - Single characters, no freq data:     10  (not in corpus / very rare)

Usage:
  python build/build_dict.py huiyang
  python build/build_dict.py huiyang --char-freq ./sources/tw-hakka-word-weighting.txt
  python build/build_dict.py huiyang --check   # validate inputs, don't write.
"""
from __future__ import annotations

import argparse
import csv
import re
import sys
from dataclasses import dataclass
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
SCHEMAS_DIR = REPO_ROOT / "schemas"
SOURCES_DIR = REPO_ROOT / "sources"

CHAR_WEIGHT_HIGH = 90     # top 10% of characters by corpus frequency
CHAR_WEIGHT_MID = 50      # next 40%
CHAR_WEIGHT_LOW = 20      # next 40%
CHAR_WEIGHT_DEFAULT = 10  # not in corpus / very rare

# Legacy constant kept for backwards-compatibility; no longer used as a
# hard-coded override — all source files now use freq_corpus lookup.
FLASHCARD_WEIGHT = 1000

# Vocabulary word tiers — derived from rime-essay corpus frequencies.
# Kept below FLASHCARD and above CHAR_HIGH so curated phrases always win
# but common words beat single-char fallbacks.
WORD_FREQ_HIGH = 500      # top 10% of words by corpus frequency
WORD_FREQ_MID = 300       # next 30%
WORD_FREQ_LOW = 150       # next 30%
WORD_DEFAULT_SHORT = 100  # ≤2 chars, not in corpus
WORD_DEFAULT_LONG = 80    # 3+ chars, not in corpus

# Kept for backwards-compat references.
CHAR_WEIGHT = CHAR_WEIGHT_DEFAULT
PHRASE_WEIGHT = FLASHCARD_WEIGHT
WORD_SHORT_WEIGHT = WORD_DEFAULT_SHORT
WORD_LONG_WEIGHT = WORD_DEFAULT_LONG

# Default location for the Rime character frequency corpus inside the repo.
# Source: https://github.com/rime/rime-essay/blob/master/essay.txt
ESSAY_DEFAULT = SOURCES_DIR / "tw-hakka-word-weighting.txt"

# Supplemental Mandarin character rank data; generated by fetch_mandarin_ranks.py.
# Backfills freq weights for characters absent from the primary corpus.
MANDARIN_RANKS_DEFAULT = SOURCES_DIR / "mandarin-char-ranks.txt"

# Hagfa Pinyim: lowercase ascii letters + optional trailing tone digit 1-6.
# Syllables are space-separated inside `code`.
CODE_RE = re.compile(r"^[a-z]+[1-6]?(?: [a-z]+[1-6]?)*$")


@dataclass(frozen=True)
class Entry:
    text: str
    code: str
    weight: int

    def line(self) -> str:
        return f"{self.text}\t{self.code}\t{self.weight}"


def _normalize_code(raw: str) -> str | None:
    """Lowercase, collapse whitespace, validate. Returns None if invalid."""
    if raw is None:
        return None
    code = " ".join(raw.strip().lower().split())
    if not code:
        return None
    if not CODE_RE.match(code):
        return None
    return code


# ---------------------------------------------------------------------------
# Character frequency corpus helpers
# ---------------------------------------------------------------------------

def _is_single_han(text: str) -> bool:
    """True if text is exactly one CJK unified ideograph."""
    return len(text) == 1 and "\u4e00" <= text <= "\u9fff"


def load_char_freq(corpus_path: Path) -> dict[str, int]:
    """Parse a Rime essay.txt-format file and return a text -> weight mapping.

    Covers BOTH single Han characters and multi-char words.

    Single characters are bucketed by percentile across all chars with freq > 0:
      top 10%  -> CHAR_WEIGHT_HIGH (90)
      next 40% -> CHAR_WEIGHT_MID  (50)
      rest     -> CHAR_WEIGHT_LOW  (20)

    Multi-char words are bucketed by percentile across all words with freq > 0:
      top 10%  -> WORD_FREQ_HIGH (500)
      next 30% -> WORD_FREQ_MID  (300)
      next 30% -> WORD_FREQ_LOW  (150)

    Entries absent from the file fall back to CHAR_WEIGHT_DEFAULT or
    WORD_DEFAULT_SHORT / WORD_DEFAULT_LONG in the respective readers.
    """
    char_freqs: dict[str, int] = {}
    word_freqs: dict[str, int] = {}

    with corpus_path.open(encoding="utf-8") as fh:
        for line in fh:
            line = line.rstrip("\n")
            if "\t" not in line:
                continue
            text, _, freq_str = line.partition("\t")
            text = text.strip()
            if not text:
                continue
            try:
                freq = int(freq_str.strip())
            except ValueError:
                continue
            if freq <= 0:
                continue
            if _is_single_han(text):
                char_freqs[text] = freq
            elif len(text) > 1:
                word_freqs[text] = freq

    weights: dict[str, int] = {}

    if char_freqs:
        sorted_cf = sorted(char_freqs.values(), reverse=True)
        n = len(sorted_cf)
        high_cut = sorted_cf[max(0, int(n * 0.10) - 1)]
        mid_cut = sorted_cf[max(0, int(n * 0.50) - 1)]
        for char, freq in char_freqs.items():
            if freq >= high_cut:
                weights[char] = CHAR_WEIGHT_HIGH
            elif freq >= mid_cut:
                weights[char] = CHAR_WEIGHT_MID
            else:
                weights[char] = CHAR_WEIGHT_LOW

    if word_freqs:
        sorted_wf = sorted(word_freqs.values(), reverse=True)
        n = len(sorted_wf)
        high_cut = sorted_wf[max(0, int(n * 0.10) - 1)]
        mid_cut = sorted_wf[max(0, int(n * 0.40) - 1)]
        for word, freq in word_freqs.items():
            if freq >= high_cut:
                weights[word] = WORD_FREQ_HIGH
            elif freq >= mid_cut:
                weights[word] = WORD_FREQ_MID
            else:
                weights[word] = WORD_FREQ_LOW

    return weights


